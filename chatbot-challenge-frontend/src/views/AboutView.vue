<h1>Über die Chatbot-Challenge</h1>

<p>
Die Chatbot-Challenge war eine im Sommersemester 2023 durchgeführte Lehrveranstaltung, die parallel an der Otto-von-Guericke-Universität Magdeburg (OVGU) und der Technischen Universität Berlin (TU Berlin) durchgeführt wurde.
Das Ziel war es, Escape-Raum Spiele mittels Chatbots zu erstellen, Teams von 4-5 Studierenden der beiden Universitäten haben dazu 14 Wochen Zeit von der ersten Idee über das Kennenlernen der technischen Systeme bis hin zum Planen, Durchführen und Auswerten von zwei Nutzerstudien. Durch die Anwendung des Rasa-Frameworks haben die Teilnehmerinnen und Teilnehmer in einem intensiven Umfeld an ihren Fähigkeiten in den Bereichen Softwareentwicklung, Conversational Interfaces, User Experience und mehr gearbeitet.
</p>

<p>
Die Chatbot-Challenge wurde im August erfolgreich abgeschlossen und hat eine beeindruckende Vielfalt interessanter Beiträge und relevanter Lernerfahrungen im Bereich der Chatbot-Entwicklung hervorgebracht. 
</p>

